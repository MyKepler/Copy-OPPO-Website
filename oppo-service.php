        <div id="collapseTwo" class="accordion-body collapse in">
          <div class="accordion-inner">
            <p>1、苹果手机想要用OPPO手机搬家对机型有哪些要求？</p>
            <p>支持新手机为ColorOS 3.0及以上版本的OPPO手机，支持iOS8及以上版本的iPhone手机。</p>
            <p>2、苹果手机使用OPPO手机搬家可以传输哪些数据？</p>
            <p>支持传输联系人、日程、照片、视频及音乐。但由于iOS系统限制和版权保护，有些在应用下载的音乐、照片等资料是无法传输的。</p>
            <p>3、什么苹果手机里的APP搬不到OPPO新机中？</p>
            <p>因为ColorOS基于Android系统，iOS与Android是两个不同平台，iOS应用不能在Android系统上运行。</p>
            <p>4、苹果手机要在哪里下载OPPO手机搬家？</p>
            <p>前往ColorOS官网扫码下载或在APP Store搜索下载手机搬家。</p>
            
