        <div id="collapse9" class="accordion-body collapse in">
          <div class="accordion-inner">
            <p>新手机必须充满电量三次吗?</p>
            <p>买了新手机，第一件事就是把手机里余电完全耗完至自动关机，然后重新充满，来回反复三四次才放心？</p>
            <p>其实大可不必这么做，现在的手机电池都是锂电池，在出厂之前就已经做了激活，不像以前的电池通过彻底的充放电来激活电池。</p>
            <p>手机用至自动关机才充电是正确的吗？</p>
            <p>对锂电池而言，“少吃多餐”，更加能够发挥出锂电池的作用。在正常使用过程中，如果觉得手机电量低了（特别是低于20%），就可以接入充电器，让手机“满血复活”。</p>
            <p>手机可以过夜充电吗？</p>
            <p>￼有人生怕整夜充电会对手机造成伤害，即使睡过去了半夜还是匆忙惊醒，把充电器拔下。</p>
            <p>其实锂电池并没有大家想象的那么脆弱，当电池充满之后，会自动切断充电电源，避免超负荷过充。也就是说，一次超长时间的充电并不会造成手机电池的严重损伤，可以安心睡个好觉啦！</p>
