    <div class="accordion" id="accordion2">
      <div class="accordion-group">
        <div class="accordion-heading">
          <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseOne">
            导致网页加载速度慢的原因有哪些
          </a>
          <a class="answer" data-toggle="collapse" data-parent="#accordion2" href="#collapseOne">
          <i class="fa fa-plus-circle fa-lg" aria-hidden="true"></i>
          </a>
        </div>
        <div id="collapseOne" class="accordion-body collapse in">
          <div class="accordion-inner">
          <br>
          <p>－上网慢－</p>
          <p>确认手机当前的信号强度（信号格），手机是在哪个网络模式（4G/3G/2G）下出现的故障，在什么环境（地铁、商城、轮船、国外）上网。</p>
          <p>－无法上网－</p>
          <p>确认手机卡是否欠费或是否属于漫游（国外）使用；手机APN设置是否确认；手机流量监控设置是否正确。</p>
          <p>－数据开关无法打开/图标不显示－</p>
          <p>确认SIM卡是否存在故障（重新插卡，换卡尝试）；排查系统异常（重启手机）。</p>
          <p>－数据信号弱－</p>
          <p>当网络仅处于2/3G网络时，手机信号相对较弱，上网速度会变慢，在人群密集的时间段尤为明显，基于网络资源是固定的，人越多人均分配的网络资源也就越少，上网则会更慢。</p>
          <p>当网络处于4G网络但速度慢，通常情况为连接不畅通，表现为数据网络图标上下箭头基本不亮，可联系运营商刷新后台数据、或咨询运营商所处位置网络情况是否良好。</p>

          <p>- 运营商限制 -</p>
          <p>许多省份运营商会进行上月的结算，需要确认手机是否处于欠费状态以至于上不了网。</p>
          <p>联通已经不对2G网络进行维护，会导致无法上网或是上网慢的情况，可能是运营商故障，可联系运营商咨询。</p>

          <p>- 设置错误 - </p>
          <p>排查APN是否设置错误，设置—双卡与移动网络—SIM卡信息—接入点名称（APN）重置为默认设置。</p>
          <p>每月流量套餐设置值过小导致无法上网。</p>

          <p>如果以上排查都无法解决问题，</p>
          <p>则需要前往OPPO客户服务中心检测处理噢！</p>

