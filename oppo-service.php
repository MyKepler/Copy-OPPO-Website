        <div id="collapse3" class="accordion-body collapse in">
          <div class="accordion-inner">
            <p>问题现象：陌生号码给R9s等机型打电话，会延迟响铃3秒（拨号端已响铃1-2声，R9s才响起铃声）</p>
            <p>原因：手机设置--电话--骚扰拦截--拦截响一声来电，此开关打开引起的，开启此开关，来电会延时3秒，这个是正常需求。</p>
            <p>解决办法：如不需要延迟响铃，关闭此开关即可。</p>
            <p>目前有此开关的机型为R9s（170112）、R9s Plus（161222）、A57（170112）、R9m（170302）、A37m（170302）。</p>
     
